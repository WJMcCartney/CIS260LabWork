
public class stringOperationsInJava {

	public static void main(String[] args) {
		String name = ("William Joseph McCartney");
		char firstInitial = name.charAt(0);
		char secondInitial = name.charAt(8);
		char thirdInitial = name.charAt(15);
		
		
		System.out.println("My name is: " + name);
		System.out.println("My initials are: " + firstInitial + secondInitial + thirdInitial);
	}

}
