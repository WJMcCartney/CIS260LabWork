package labFive;

public class Student {
	private String name;
	private int age;
	private int year;
	private String ID;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getYear() {
		return year;
	}

	public void setID(String ID) {
		this.ID= ID;
	}

	public String getID() {
		return ID;
	}
}